# In this script, 'fname' is a variable that stands for the user's first name.
# While 'lname' stands for the user's last name.
# The user is asked to input their first name and last name.
fname = input("Enter your first name: ")
lname = input("Enter your last name: ")

# As a result, this script will print the user's first and last name.
# Formatted print was used in this script to print the user's first and last name including the greeting, 'Hi'.
print(f"Hi,{fname and lname}")